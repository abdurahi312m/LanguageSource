import androidx.compose.ui.unit.dp

@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun ProverbsScreen(
    onBackClick: () -> Unit
) {
    val proverbs = listOf(
        "Өнөр алды — кызыл тил." to "Первое из искусств — красноречие.",
        "Тил досту да табат, касты да табат." to "Язык найдет и друга, и врага.",
        "Билимдүүгө дүйнө жарык." to "Для знающего мир светел.",
        "Эне тили — эне сүтү." to "Родной язык — молоко матери.",
        "Аз сөз — алтын, көп сөз — жез." to "Меньше слов — золото, больше слов — медь."
    )

    Scaffold(
        topBar = {
            TopAppBar(
                title = { Text("Макалдар") },
                navigationIcon = {
                    IconButton(onClick = onBackClick) {
                        Icon(Icons.Default.ArrowBack, contentDescription = "Назад")
                    }
                }
            )
        }
    ) { padding ->
        LazyColumn(
            modifier = Modifier
                .fillMaxSize()
                .padding(padding),
            contentPadding = PaddingValues(16.dp),
            verticalArrangement = Arrangement.spacedBy(12.dp)
        ) {
            items(proverbs) { (kg, ru) ->
                Card(
                    modifier = Modifier.fillMaxWidth(),
                    colors = CardDefaults.cardColors(
                        containerColor = MaterialTheme.colorScheme.surfaceVariant.copy(alpha = 0.3f)
                    )
                ) {
                    Column(modifier = Modifier.padding(16.dp)) {
                        Text(
                            text = kg,
                            style = MaterialTheme.typography.titleMedium,
                            fontWeight = FontWeight.Bold
                        )
                        Spacer(modifier = Modifier.height(4.dp))
                        Text(
                            text = ru,
                            style = MaterialTheme.typography.bodyMedium,
                            color = MaterialTheme.colorScheme.onSurfaceVariant
                        )
                    }
                }
            }
        }
    }
}